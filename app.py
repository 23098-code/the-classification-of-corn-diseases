class_names = [
    "Blight (‡πÇ‡∏£‡∏Ñ‡πÉ‡∏ö‡πÑ‡∏´‡∏°‡πâ)",
    "Common Rust (‡πÇ‡∏£‡∏Ñ‡∏£‡∏≤‡∏™‡∏ô‡∏¥‡∏°)",
    "Grey Leaf Spot (‡πÇ‡∏£‡∏Ñ‡πÉ‡∏ö‡∏à‡∏∏‡∏î‡∏™‡∏µ‡πÄ‡∏ó‡∏≤)",
    "Healthy (‡πÉ‡∏ö‡∏õ‡∏Å‡∏ï‡∏¥)"
]

# -----------------------------
# ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô crop ‡∏£‡∏π‡∏õ (‡∏ã‡∏π‡∏°‡πÄ‡∏Ç‡πâ‡∏≤‡πÅ‡∏£‡∏á‡∏Ç‡∏∂‡πâ‡∏ô)
# -----------------------------
def aggressive_center_crop(img, crop_ratio=0.6):
    """
    crop_ratio = 0.6 ‡∏´‡∏°‡∏≤‡∏¢‡∏ñ‡∏∂‡∏á ‡∏ï‡∏±‡∏î‡∏Ç‡∏≠‡∏ö‡∏≠‡∏≠‡∏Å 40%
    """
    w, h = img.size
    new_w = int(w * crop_ratio)
    new_h = int(h * crop_ratio)

    left = (w - new_w) // 2
    top = (h - new_h) // 2
    right = left + new_w
    bottom = top + new_h

    return img.crop((left, top, right, bottom))

# -----------------------------
# ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏°‡∏†‡∏≤‡∏û‡πÄ‡∏Ç‡πâ‡∏≤‡πÇ‡∏°‡πÄ‡∏î‡∏•
# -----------------------------
def preprocess_image(img):
    img = img.resize((128, 128))  # ‚ö†Ô∏è ‡∏ï‡πâ‡∏≠‡∏á‡∏ï‡∏£‡∏á‡∏Å‡∏±‡∏ö‡∏ï‡∏≠‡∏ô‡πÄ‡∏ó‡∏£‡∏ô
    img = np.array(img) / 255.0
    img = np.expand_dims(img, axis=0)
    return img

# -----------------------------
# ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ß‡∏¥‡∏ò‡∏µ‡∏£‡∏±‡∏ö‡∏†‡∏≤‡∏û
# -----------------------------
option = st.radio(
    "‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏ß‡∏¥‡∏ò‡∏µ‡πÉ‡∏™‡πà‡∏†‡∏≤‡∏û",
    ["üì§ ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏†‡∏≤‡∏û", "üì∏ ‡∏ñ‡πà‡∏≤‡∏¢‡∏†‡∏≤‡∏û‡∏à‡∏≤‡∏Å‡∏Å‡∏•‡πâ‡∏≠‡∏á"]
)

uploaded_image = None

if option == "üì§ ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏†‡∏≤‡∏û":
    uploaded_image = st.file_uploader(
        "‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡πÑ‡∏ü‡∏•‡πå‡∏†‡∏≤‡∏û",
        type=["jpg", "jpeg", "png", "jfif", "webp"]
    )
else:
    uploaded_image = st.camera_input("‡∏ñ‡πà‡∏≤‡∏¢‡∏†‡∏≤‡∏û‡πÉ‡∏ö‡∏Ç‡πâ‡∏≤‡∏ß‡πÇ‡∏û‡∏î")

# -----------------------------
# ‡∏ñ‡πâ‡∏≤‡∏°‡∏µ‡∏†‡∏≤‡∏û ‚Üí ‡πÅ‡∏™‡∏î‡∏á + ‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå
# -----------------------------
if uploaded_image is not None:
    image = Image.open(uploaded_image).convert("RGB")

    st.subheader("üì∑ ‡∏†‡∏≤‡∏û‡∏ï‡πâ‡∏ô‡∏â‡∏ö‡∏±‡∏ö")
    st.image(image, use_container_width=True)

    # crop ‡πÅ‡∏£‡∏á
    cropped_image = aggressive_center_crop(image, crop_ratio=0.6)

    st.subheader("‚úÇÔ∏è ‡∏†‡∏≤‡∏û‡∏´‡∏•‡∏±‡∏á‡∏Ñ‡∏£‡∏≠‡∏õ (‡∏ã‡∏π‡∏°‡πÄ‡∏Ç‡πâ‡∏≤‡πÉ‡∏ö)")
    st.image(cropped_image, use_container_width=True)

    if st.button("üîç ‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå‡πÇ‡∏£‡∏Ñ"):
        try:
            img_array = preprocess_image(cropped_image)
            prediction = model.predict(img_array)

            confidence = float(np.max(prediction))
            predicted_class = int(np.argmax(prediction))

            st.subheader("üìä ‡∏ú‡∏•‡∏Å‡∏≤‡∏£‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå")

            # ‡∏ñ‡πâ‡∏≤‡∏Ñ‡∏ß‡∏≤‡∏°‡∏°‡∏±‡πà‡∏ô‡πÉ‡∏à‡∏ï‡πà‡∏≥‡∏Å‡∏ß‡πà‡∏≤ 50% ‚Üí ‡πÑ‡∏°‡πà‡∏ü‡∏±‡∏ô‡∏ò‡∏á
            if confidence < 0.50:
                st.warning("‚ö†Ô∏è ‡∏Ñ‡∏ß‡∏≤‡∏°‡∏°‡∏±‡πà‡∏ô‡πÉ‡∏à‡∏ï‡πà‡∏≥‡∏Å‡∏ß‡πà‡∏≤ 50% ‡πÅ‡∏ô‡∏∞‡∏ô‡∏≥‡πÉ‡∏´‡πâ‡∏ñ‡πà‡∏≤‡∏¢‡∏†‡∏≤‡∏û‡πÉ‡∏´‡∏°‡πà‡πÉ‡∏´‡πâ‡πÄ‡∏´‡πá‡∏ô‡πÉ‡∏ö‡∏ä‡∏±‡∏î‡∏Ç‡∏∂‡πâ‡∏ô")
            else:
                st.success(f"üå± ‡∏ú‡∏•‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏ô‡∏≤‡∏¢: **{class_names[predicted_class]}**")
                st.write(f"üìä ‡∏Ñ‡∏ß‡∏≤‡∏°‡∏°‡∏±‡πà‡∏ô‡πÉ‡∏à: **{confidence*100:.2f}%**")

            st.subheader("üìà ‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ô‡πà‡∏≤‡∏à‡∏∞‡πÄ‡∏õ‡πá‡∏ô‡πÅ‡∏ï‡πà‡∏•‡∏∞‡∏Ñ‡∏•‡∏≤‡∏™")
            for i, prob in enumerate(prediction[0]):
                st.write(f"- {class_names[i]}: {prob*100:.2f}%")

        except Exception as e:
            st.error(f"‚ùå ‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå: {e}")
